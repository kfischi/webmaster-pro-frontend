function DeviceFrame({ view, orientation, zoom, children }) {
  const getFrameStyle = () => {
    switch (view) {
      case 'tablet':
        return {
          width: orientation === 'portrait' ? '768px' : '1024px',
          height: orientation === 'portrait' ? '1024px' : '768px',
          borderRadius: '24px',
          border: '12px solid #6b7280',
          backgroundColor: '#6b7280',
          boxShadow: '0 20px 60px rgba(0,0,0,0.4)',
          position: 'relative',
          overflow: 'hidden'
        };
      case 'mobile':
        return {
          width: orientation === 'portrait' ? '375px' : '667px',
          height: orientation === 'portrait' ? '812px' : '375px',
          borderRadius: '36px',
          border: '8px solid #1f2937',
          backgroundColor: '#1f2937',
          boxShadow: '0 25px 70px rgba(0,0,0,0.6)',
          position: 'relative',
          overflow: 'hidden'
        };
      default:
        return {
          width: '100%',
          height: '100%',
          borderRadius: '12px',
          overflow: 'hidden',
          boxShadow: '0 10px 40px rgba(0,0,0,0.2)'
        };
    }
  };

  const getNotchStyle = () => {
    if (view === 'mobile' && orientation === 'portrait') {
      return {
        position: 'absolute',
        top: '-8px',
        left: '50%',
        transform: 'translateX(-50%)',
        width: '150px',
        height: '32px',
        backgroundColor: '#1f2937',
        borderRadius: '0 0 20px 20px',
        zIndex: 10,
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center'
      };
    }
    return {};
  };

  const getCameraStyle = () => {
    if (view === 'mobile' && orientation === 'portrait') {
      return {
        position: 'absolute',
        top: '5px',
        left: '50%',
        transform: 'translateX(-50%)',
        width: '8px',
        height: '8px',
        backgroundColor: '#374151',
        borderRadius: '50%',
        zIndex: 11
      };
    }
    return {};
  };

  return (
    <div style={getFrameStyle()}>
      {view === 'mobile' && orientation === 'portrait' && (
        <>
          <div style={getNotchStyle()}></div>
          <div style={getCameraStyle()}></div>
        </>
      )}
      <div style={{
        width: '100%',
        height: '100%',
        overflow: 'auto',
        borderRadius: view === 'desktop' ? '12px' : '20px',
        transform: view === 'desktop' ? `scale(${zoom / 100})` : 'scale(1)',
        transformOrigin: 'top left'
      }}>
        {children}
      </div>
    </div>
  );
}

